// Pomocné funkcie pre filtrovanie
export const getPlayersByCategory = (category: Player['category']): Player[] => {
  return players
    .filter(p => p.category === category && p.isActive)
    .sort((a, b) => {
      // Najprv zoradiť podľa pozície
      const positionOrder = ['Brankár', 'Obranca', 'Stredný záložník', 'Útočník'];
      const posA = positionOrder.indexOf(a.position);
      const posB = positionOrder.indexOf(b.position);
      
      if (posA !== posB) {
        return posA - posB;
      }
      
      // Potom abecedne podľa priezviska
      return a.lastName.localeCompare(b.lastName, 'sk');
    });
};

export const getPlayerById = (id: string): Player | undefined => {
  return players.find(p => p.id === id);
};

export const getPlayersByPosition = (position: Player['position']): Player[] => {
  return players.filter(p => p.position === position && p.isActive);
};
