-- PUBLIC READ ACCESS (Všetci môžu vidieť)
CREATE POLICY "Public read articles" ON public.articles FOR SELECT USING (is_published = TRUE);
CREATE POLICY "Public read videos" ON public.videos FOR SELECT USING (TRUE);
CREATE POLICY "Public read players" ON public.players FOR SELECT USING (is_active = TRUE);
CREATE POLICY "Public read officials" ON public.officials FOR SELECT USING (TRUE);
CREATE POLICY "Public read partners" ON public.partners FOR SELECT USING (TRUE);

-- CONTACT FORM (Vkladanie správ)
CREATE POLICY "Public insert contact messages" ON public.contact_messages FOR INSERT WITH CHECK (TRUE);

